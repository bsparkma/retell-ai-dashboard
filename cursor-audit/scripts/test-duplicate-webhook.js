#!/usr/bin/env node
/**
 * test-duplicate-webhook.js
 *
 * Confirms or refutes LK-01 in cursor-audit/11-evidence-and-confidence.md:
 *   "Duplicate Retell webhooks may produce duplicate CommLog rows."
 *
 * What it does:
 *   1. POSTs a synthetic `call_analyzed` webhook to the backend twice in quick succession.
 *   2. Reads `data/unified_calls.json` after each POST to inspect `od_sync_status`.
 *   3. Reports whether the in-memory dedup actually held.
 *
 * What it cannot do from outside the office network:
 *   - Verify the Open Dental CommLog table directly. The script flags the call_id
 *     it used so the office can SELECT * FROM commlog WHERE ExternalID = ? after
 *     the run and report whether 1 or 2 rows landed.
 *
 * Notes:
 *   - The script targets http://localhost:5000 by default. Override with WEBHOOK_TARGET.
 *   - This script does NOT compute a real Retell HMAC signature. Run with NODE_ENV=development
 *     on the backend, OR temporarily relax verification, OR run on a backend that has been
 *     fixed per B-P0-02 (in which case set RETELL_API_KEY to the same value the backend uses).
 *
 *   - Output: cursor-audit/scripts/results/test-duplicate-webhook-<ts>.json
 */

const fs = require('fs');
const path = require('path');
const http = require('http');
const https = require('https');
const { URL } = require('url');
const crypto = require('crypto');

// -- Config ------------------------------------------------------------------

const TARGET = process.env.WEBHOOK_TARGET || 'http://localhost:5000/api/webhooks/retell';
const REPO_ROOT = path.resolve(__dirname, '..', '..');
const UNIFIED_CALLS_PATH = path.join(REPO_ROOT, 'data', 'unified_calls.json');
const RESULTS_DIR = path.join(__dirname, 'results');
const TEST_CALL_ID = `audit_dup_test_${Date.now()}`;
const SLEEP_BETWEEN_MS = 500; // simulate Retell's retry timing
const SLEEP_AFTER_FINAL_MS = 3000;

// -- Synthetic webhook payload ----------------------------------------------

function buildAnalyzedPayload() {
  return {
    event: 'call_analyzed',
    call: {
      call_id: TEST_CALL_ID,
      from_number: '+15555550199',
      to_number: '+15555550100',
      start_timestamp: new Date(Date.now() - 60_000).toISOString(),
      end_timestamp: new Date().toISOString(),
      duration_ms: 60_000,
      transcript: 'AUDIT TEST: This is a synthetic call generated by ' +
                  'cursor-audit/scripts/test-duplicate-webhook.js. ' +
                  'It exists to verify that duplicate Retell webhooks ' +
                  'do not produce duplicate CommLog rows. Safe to delete.',
      call_analysis: {
        call_summary: 'Audit test — synthetic duplicate webhook check.',
        user_sentiment: 'Neutral',
        call_successful: true,
      },
    },
  };
}

// -- HTTP --------------------------------------------------------------------

function postJson(targetUrl, body, headers = {}) {
  return new Promise((resolve, reject) => {
    const u = new URL(targetUrl);
    const lib = u.protocol === 'https:' ? https : http;
    const data = Buffer.from(JSON.stringify(body));

    const req = lib.request(
      {
        method: 'POST',
        hostname: u.hostname,
        port: u.port || (u.protocol === 'https:' ? 443 : 80),
        path: u.pathname + (u.search || ''),
        headers: {
          'Content-Type': 'application/json',
          'Content-Length': data.length,
          'User-Agent': 'cursor-audit/test-duplicate-webhook.js',
          ...headers,
        },
        timeout: 10_000,
      },
      (res) => {
        const chunks = [];
        res.on('data', (c) => chunks.push(c));
        res.on('end', () => {
          resolve({
            status: res.statusCode,
            headers: res.headers,
            body: Buffer.concat(chunks).toString('utf8'),
          });
        });
      },
    );

    req.on('error', reject);
    req.on('timeout', () => req.destroy(new Error('request timeout')));
    req.write(data);
    req.end();
  });
}

// -- Inspection helpers ------------------------------------------------------

function readUnifiedCalls() {
  try {
    const raw = fs.readFileSync(UNIFIED_CALLS_PATH, 'utf8');
    return JSON.parse(raw);
  } catch (e) {
    return { error: e.message };
  }
}

function findCallById(store, externalId) {
  if (!store || !Array.isArray(store.calls)) return null;
  return store.calls.find(
    (c) =>
      c.id === externalId ||
      c.external_id === externalId ||
      c.call_id === externalId,
  );
}

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

// -- Main --------------------------------------------------------------------

(async () => {
  console.log('=== LK-01 duplicate-webhook verification ===');
  console.log(`Target:    ${TARGET}`);
  console.log(`Call ID:   ${TEST_CALL_ID}`);
  console.log(`Unified:   ${UNIFIED_CALLS_PATH}`);
  console.log('');

  const payload = buildAnalyzedPayload();
  const results = {
    target: TARGET,
    callId: TEST_CALL_ID,
    startedAt: new Date().toISOString(),
    posts: [],
    snapshots: [],
    interpretation: null,
  };

  for (let i = 1; i <= 2; i++) {
    console.log(`POST #${i} ...`);
    try {
      const res = await postJson(TARGET, payload);
      console.log(`  status=${res.status} body=${res.body.slice(0, 200)}`);
      results.posts.push({ attempt: i, status: res.status, body: res.body });
    } catch (e) {
      console.log(`  ERROR ${e.message}`);
      results.posts.push({ attempt: i, error: e.message });
    }
    await sleep(SLEEP_BETWEEN_MS);
  }

  console.log('');
  console.log(`Sleeping ${SLEEP_AFTER_FINAL_MS}ms for sync to settle ...`);
  await sleep(SLEEP_AFTER_FINAL_MS);

  const store = readUnifiedCalls();
  const call = findCallById(store, TEST_CALL_ID);

  results.snapshots.push({
    label: 'final',
    storeFound: !!store && !store.error,
    callFound: !!call,
    od_sync_status: call?.od_sync_status,
    od_commlog_num: call?.od_commlog_num,
    od_sync_attempted_at: call?.od_sync_attempted_at,
    od_synced_at: call?.od_synced_at,
    od_match_result: call?.od_match_result,
    od_sync_error: call?.od_sync_error,
  });

  // -- Interpretation --
  const status = call?.od_sync_status;
  let verdict;
  if (!call) {
    verdict = {
      result: 'INCONCLUSIVE',
      reason:
        'The synthetic call did not appear in unified_calls.json. ' +
        'This usually means the backend rejected the request (signature, auth, or 401/500). ' +
        'Check the response body above and the backend logs.',
    };
  } else if (status === 'synced') {
    verdict = {
      result: 'PARTIAL_DEDUP_HELD',
      reason:
        'The call was marked synced after the first POST. The second POST should have ' +
        'short-circuited via the `od_sync_status === "synced"` check in openDentalSync.js:184. ' +
        'BUT this only holds if the office\'s OD CommLog table actually has exactly one row ' +
        'for this call_id. To finish the test, run in OD: ' +
        `SELECT COUNT(*) FROM commlog WHERE Note LIKE '%${TEST_CALL_ID}%';  -- expect 1`,
    };
  } else if (status === 'pending_match') {
    verdict = {
      result: 'NO_PATIENT_MATCH',
      reason:
        'The synthetic phone (+15555550199) did not match any patient in OD. ' +
        'Sync was skipped; this test cannot exercise the duplicate-CommLog code path. ' +
        'Re-run with a phone number that is in your OD patient table.',
    };
  } else if (status === 'error') {
    verdict = {
      result: 'SYNC_ERRORED',
      reason: `Sync errored: ${call.od_sync_error}. Check backend logs.`,
    };
  } else {
    verdict = {
      result: 'UNEXPECTED',
      reason: `od_sync_status was '${status}'. Inspect manually.`,
    };
  }

  results.interpretation = verdict;
  results.finishedAt = new Date().toISOString();

  console.log('');
  console.log('=== VERDICT ===');
  console.log(`  ${verdict.result}`);
  console.log(`  ${verdict.reason}`);
  console.log('');

  // -- Persist results --
  fs.mkdirSync(RESULTS_DIR, { recursive: true });
  const outPath = path.join(
    RESULTS_DIR,
    `test-duplicate-webhook-${Date.now()}.json`,
  );
  fs.writeFileSync(outPath, JSON.stringify(results, null, 2));
  console.log(`Results written to ${outPath}`);

  // -- Office follow-up reminder --
  console.log('');
  console.log('NEXT STEP (office side):');
  console.log(
    `  Run this in Open Dental to confirm exactly one CommLog row was inserted:`,
  );
  console.log(
    `    SELECT COUNT(*) FROM commlog WHERE Note LIKE '%${TEST_CALL_ID}%';`,
  );
  console.log(`  Expected: 1.   If it returns 2, LK-01 is CONFIRMED.`);
})().catch((e) => {
  console.error('Script crashed:', e);
  process.exit(1);
});
